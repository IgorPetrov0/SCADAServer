#include "condition.h"

condition::condition()
{
    targetObject=nullptr;
    targetPort=nullptr;
}
//////////////////////////////////////////////////////////////////////
QString condition::getName() const{
    return name;
}
//////////////////////////////////////////////////////////////////////
void condition::setName(const QString &value){
    name = value;
}
//////////////////////////////////////////////////////////////////////
QString condition::getDescription() const{
    return description;
}
//////////////////////////////////////////////////////////////////////
void condition::setDescription(const QString &value){
    description = value;
}
//////////////////////////////////////////////////////////////////////
object *condition::getTargetObject() const{
    return targetObject;
}
//////////////////////////////////////////////////////////////////////
void condition::setTargetObject(object *value){
    targetObject = value;
}
//////////////////////////////////////////////////////////////////////
objectPort *condition::getTargetPort() const{
    return targetPort;
}
//////////////////////////////////////////////////////////////////////
void condition::setTargetPort(objectPort *value){
    targetPort = value;
}
//////////////////////////////////////////////////////////////////////
bool condition::getPortState() const{
    return portState;
}
//////////////////////////////////////////////////////////////////////
void condition::setPortState(bool value){
    portState = value;
}
//////////////////////////////////////////////////////////////////////
QTime condition::getTime() const{
    return time;
}
//////////////////////////////////////////////////////////////////////
void condition::setTime(const QTime &value){
    time = value;
}
//////////////////////////////////////////////////////////////////////
logicType condition::getLogic() const{
    return logic;
}
///////////////////////////////////////////////////////////////////////
QString condition::getLogicString(){
    switch(logic){
        case(LOGIC_AND):{
            return QObject::tr("И");
        }
        case(LOGIC_NO):{
            return QObject::tr("НЕ");
        }
        case(LOGIC_OR):{
            return QObject::tr("ИЛИ");
        }
    }
}
//////////////////////////////////////////////////////////////////////
void condition::setLogic(const logicType &value){
    logic = value;
}
